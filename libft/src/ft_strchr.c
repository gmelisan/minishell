#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	char	cc;
	size_t	i;

	cc = (char)c;
	i = 0;
	while (s[i])
	{
		if (s[i] == cc)
			return ((char *)&s[i]);
		i++;
	}
	if (c == '\0')
		return ((char *)&s[i]);
	else
		return (NULL);
}
